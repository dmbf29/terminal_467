pwd
-> prints current directory

ls / ll
-> lists the contents of directory

cd directory
-> moves into a directory

cd ..
  -> moves backwards

mkdir
-> creates new directory

touch
-> creates new file

rm filename
-> removes file

rm -r directory
-> removes directory

rm -f something
-> force removes

mv file_name directory
-> moves into directory

mv file_name new_file_name
-> renames
